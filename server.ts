// Usage statistics for live Gemini Quota/Limits dashboard
const usageStats = {
  totalRequests: 0,
  ttsRequests: 0,
  transliterateRequests: 0,
  enhanceRequests: 0,
  estimatedTokens: 0,
  lastRequestTime: null as string | null,
};

// Generates a beautiful 16-bit linear PCM little-endian tone blend that acts 
// as high-quality offline audio fallback if the live Google GenAI API is rate-limited.
function generateProceduralPCM(durationSeconds = 1.0, sampleRate = 24000): string {
  const numSamples = Math.floor(sampleRate * durationSeconds);
  const buffer = Buffer.alloc(numSamples * 2);
  
  for (let i = 0; i < numSamples; i++) {
    const t = i / sampleRate;
    // Elegant bell-like chime: Fundamental sine + a sweet minor seventh / perfect fifth overtone
    const freq1 = 440; // Pure A4 pitch
    const freq2 = 659.25; // E5 perfect fifth
    
    // Linear decay curve for a clean acoustic fade
    const decay = Math.max(0, 1 - (t / durationSeconds));
    const wave = (Math.sin(2 * Math.PI * freq1 * t) * 0.6 + Math.sin(2 * Math.PI * freq2 * t) * 0.3) * decay;
    
    // Convert float value [-1.0, 1.0] to signed 16-bit integer [-32768, 32767]
    const intSample = Math.floor(wave * 32767);
    buffer.writeInt16LE(intSample, i * 2);
  }
  
  return buffer.toString("base64");
}

// Lazy initialisation of the Google GenAI SDK to guard against missing API keys
let aiClient: GoogleGenAI | null = null;
function getGeminiClient(): GoogleGenAI {
  if (!aiClient) {
    const key = process.env.GEMINI_API_KEY;
    if (!key) {
      throw new Error("GEMINI_API_KEY is not configured in the Secrets panel. Please configure it in the application's Secret Settings.");
    }
    aiClient = new GoogleGenAI({
      apiKey: key,
      httpOptions: {
        headers: {
          "User-Agent": "aistudio-build",
        },
      },
    });
  }
  return aiClient;
}

