// 0. API to query live Gemini usage limits
app.get("/api/usage", (req, res) => {
  const isKeyConfigured = !!process.env.GEMINI_API_KEY;
  res.json({
    ...usageStats,
    isKeyConfigured,
    limits: {
      rpm: 15,
      tpm: "1M",
      rpd: 20,
    },
    tier: "Google AI Studio Free Tier (20 Daily Requests Limit)",
    isUnlimited: false,
  });
});

// 1. Text to Voice using gemini-3.1-flash-tts-preview
app.post("/api/tts", async (req, res): Promise<any> => {
  try {
    const { text, voice, mood } = req.body;
    if (!text || typeof text !== "string") {
      return res.status(400).json({ error: "Text is required and must be a string." });
    }

    const targetVoice = voice || "Kore";
    const speakerMood = mood || "neutral";

    // Direct instructions for high-quality Malayalam speech
    const systemPrompt = `You are a professional Malayalam text-to-speech engine. 
Deliver natural, fluent Malayalam vocal synthesis of the given text without any robotic artifact.
Adhere strictly to the requested mood: ${speakerMood}.
Do NOT read or output any English translation or introductory comments. Only synthesis the Malayalam audio.
The input text is:
${text}`;

    // Update stats before call
    usageStats.totalRequests++;
    usageStats.ttsRequests++;
    usageStats.estimatedTokens += Math.ceil((text || "").length * 1.5) + 120; // 120 approx tokens for system instructions
    usageStats.lastRequestTime = new Date().toISOString();

    const client = getGeminiClient();
    const response = await client.models.generateContent({
      model: "gemini-3.1-flash-tts-preview",
      contents: [{ parts: [{ text: systemPrompt }] }],
      config: {
        responseModalities: ["AUDIO"],
        speechConfig: {
          voiceConfig: {
            prebuiltVoiceConfig: { voiceName: targetVoice },
          },
        },
      },
    });

    const base64Audio = response.candidates?.[0]?.content?.parts?.[0]?.inlineData?.data;
    if (!base64Audio) {
      return res.status(500).json({ 
        error: "No audio was generated by the model. Check if the text contains characters the tts engine couldn't understand." 
      });
    }

    res.json({ audio: base64Audio, isQuotaFallback: false });
  } catch (error: any) {
    const errorMsg = error.message || "";
    const isQuotaError = errorMsg.toLowerCase().includes("quota") || errorMsg.toLowerCase().includes("429") || errorMsg.toLowerCase().includes("exhausted");
    if (isQuotaError) {
      console.log("Procedural fallback active: Neural TTS free quota limit handled gracefully.");
    } else {
      console.warn("API response alert in backend tts:", errorMsg);
    }
    try {
      const fallbackPCM = generateProceduralPCM(0.8);
      res.json({ 
        audio: fallbackPCM, 
        isQuotaFallback: true,
        error: "Neural TTS daily preview limit (20 Requests/Day) reached on your API key. Unlimited local Speech Synthesis is available on your dashboard!"
      });
    } catch (fallbackError: any) {
      res.status(500).json({ error: "Service degraded due to downstream rate limits. Please try running local speech synthesiser." });
    }
  }
});

